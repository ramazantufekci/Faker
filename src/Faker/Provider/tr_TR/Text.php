    HAYIZ ve NİFAS 
    
    
    Ve dahi mesele-i hayzın (hayız meselesinin) ekalli (en azı) üç gündür, ekseri (en çoğu) on 
    gündür. Nifasın (lohusalığın) ekalline had yoktur; ne vakit kesilir ise gusül edip namaz kılana 
    kılmak gerek ve oruç tutmak gerek. Ekseri kırk gündür. Eğer kan üç günden eksik de kesilse 
    hayız sanıp namazın(ı) kılmadı ise kaza ede, gusül lazım değildir. Ve eğer üç (gün) tamam 
    oldukta kesilse gusül edip vakit namazın(1) kıla. On gün tamam oldukta kesilse de ve kesilmese 
    de gusül edip namazın(ı) kıla. 
    
    Nifasın kırk günü tamam olunca kıla. Gusül ettikde kesilse de kesilmese de hayız ve nifas 
    günlerinde her ne türlü kan gelirse safi kan hükmündedir, -san olsun, bulanık olsun-. Hayzın on 
    günü içinde (ve)ya nifasın kırk günü içinde bir iki gün kan gelmese, kesildi sanıp, gusül edip 
    oruç tutsa, sonra geri müddet içinde kan gelse ol orucu kaza etmek gerektir, kesildikte geri 
    (yine) gusül etmek gerek. Eğer adetinden evvel kesilse lakin üç günden sonra olsa gusül edip 
    namazın(1) kıla, lakin eri ile cima etmeye. Adeti geçmeyince nifas dahi böyledir. Eğer adetinden 
    ziyadede kesilse lakin on günde ya dahi eksikte kesilse hep hayızdır. Eğer on gün tamam 
    oldukta kesilmeyip aksa, adetinden ilerisi hayız olmaz lakin ol günlerin namazların(ı) kaza ede. 
    Nifasın kırkı dahi hayzın onu gibidir. 
    
    Ramazanda sabah yeri ağardıktan sonra hayız ve nifas kesilse ol günü yemeye ve içmeye, 
    ımsak ede lakin oruç olmaz, kazası lazımdır. Ve eğer irte (tan) yeri ağardıktan sonra kan gelse 
    ol günü yiye ve içe. Eğer ikindiden sonra görürse de ve bilcümle bir avret kan görse namazdan 
    ve oruçtan vaz gele. Ve eğer üç gün olmadan kesilse, namazın âhır (son) vaktine dek sabr ede, 
    vaktinde geri kan görse namaz kılmaya ve eğer gelmez ise abdest ala, namazın(1) kıla, eğer geri 
    kan gelse yine namazdan vaz geçe. Eğerçi kesilse yine namazın âhır vaktine dek eğleye, gelmez 
    ise abdest ala, namazın(ı) kıla. Üç güne dek böyle eyleye; gusül lazım değildir, yalnız abdest 
    kifayet eder. Üç günden sonra kesilse geri namazın âhır (son) vaktine dek eğlene, gelmez ise 
    gusül ede namazın(1) kıla, gelir ise namazdan vaz geçe. Kıyas üzere on güne varınca ondan 
    sonra gusül ede, namazın(1) kıla, kan akarsa da. Nifas da böyledir. Lakin her kesildikçe gusül 
    lazımdır, bir günde kesilir ise de. Eğer irte yeri ağarmadan kesilse niyet edip orucun(u) tuta, 
    eğer kuşluğun (ve)ya ikindiden sonra geri kan gelse ol oruç olmaz, ol gün(ü) yine sonra kaza 
    ede. 
    
    
    Eğer düşük düşerse parmağı ya saçı ya ağızı ya bumu belli olsa bütün oğlan doğurmuş gibi 
    olur. Eğer hiç bir yeri belli değilse nifas olmaz lakin üç gün (ve)ya dahi ziyade akarsa hayız 
    olur. Eğer hayızdan eksileli on beş gün (ve)ya dahi ziyade olup da düşürdü ise eğer üç günden 
    eksik de kesilse (ve)ya hayız kesileli on beş gün olmamış olsa hayız değildir, burun kanı gibidir; 
    namazın(1) kıla ve orucun(u) tuta, eriyle yata, gusül lazım değildir. Mesele: Vakti içinde namaz 
    kılmazdan evvel hatun hayzını görse ol vakit namazı kaza etmek lazım değildir, -Hulasâtü'l- 
    Fetâva'da Kitab-ı hayzın fasl-ı sânisinde mezkürdur-. 
    
    
