    ŞEHADET OKUMANIN FAYDASI 
    
    
    Ömründe bir kere şehadet getirmek farzdır, delil Allah Taâlâ'nın "Bilki Allah'tan başka 
    tanrı yoktur" (Muhammed 47/19) kavl-i şerifidir. Ve kelime-i şehadet getirmenin dört şartı 
    vardır: 
    
    1. Dilile getirirken kalbi hazır olmak, 
    
    2. Mânasını bilmek, 
    
    3. Hulüs-i kalp ile getirmek, 
    
    4. Tazim ile getirmek. 
    
    Ve dahi şehadet getirmenin yüz otuz kadar faidesi vardır. Amma zikr olunacak dört şeyden 
    biri bulunmasa şehadet kelimesinin faidesi olur, eğer bulunursa faidesi yoktur. Ol dört şeyin biri 
    şirk (Allah'a ortak koşmak), biri şek (şüphe), biri teşbih (Yüce Allah'ı varlıklardan birine 
    benzetmek), biri ta'til (Allah'ın varlığını veya kâinata müdahalesini inkâr etmek). Şirk Allahu 
    azimü'ş-şan'ın zatında şirk koşmağa, şek dinde meks etmeğe (tereddüt göstererek durmaya), 
    teşbih vehminde Allah Taâlâ'yı bir mahlüka benzetmeğe, ta'til Allahu azimü'ş-şan âleme 
    karışmaz, her bir şey vakti geldikte kendi tabiatıyla olur demeğe derler. 
    
    Ve dahi yüz otuz faidenin otuzu bu mahalde zikr olunmuştur. Otuzdan beşi dünyada ve beşi 
    ölür iken ve beşi kabirde ve beşi Arasat'ta ve beşi Cehennem'de ve beşi Cennet'te. 
    
    Amma dünyada olan beş faide: İsmi güzel çağırılır, ahkâm-ı şer'iyye üzerine farz olur, 
    boynu kılıçtan kurtulur, Allahu azimü'ş-şan razı olur, cümle müminler ona muhabbet eder. 
    
    Ve dahi ölür iken olan beş faide: Azrail aleyhı's-selâm ona güzel surette gelir, san yağdan 
    kıl çeker gibi ruhunu alır, Cennet kokuları gelir ve dahi İlliyyin'e çıkar müjdeci melekler gelir 
    "merhaba ya mümin, sen Cennetliksin"deyü haber verirler. 
    
    
    Ve dahi kabirde olan beş faide: Kabri geniş olur, Münker ve Nekir hazretleri güzel surette 
    gelir, Allahu azimü'ş-şan ona bir melek tayin eder, bilmediğini talim eder, Allahu azimü'ş-şan 
    bilmediğini hatırına getirir, Cennet'te makamın(1) görür. 
    
    Ve dahi meydan-ı Arasat'ta olan beş faide: Sual ve hesabı asan (kolay) olur, kitabı sağından 
    verilir, mizanda sevabı ağır gelir, Arşu'r-Rahman altında gölgelenir, Sırat'ı yıldırım gibi geçer. 
    
    Ve dahi Cehennem'de olan beş faide; Eğer Cehennem'e girer ise Cehennem ehli gibi gözleri 
    gök olmaz, şeytan ile çatılmaz, ellerine ateşten bilecek, dahi boğazına zencir vurulmaz, Hamım 
    suyundan içirmezler, ebedi Cehennem'de kalmaz. 
    
    Ve dahi Cennet'te olan beş faide: Cümle melekler ona selâm verir, sıddiklar ile refik 
    (arkadaş) olur, ebedi Cennet'te kalır, Allah Taâlâ ondan razı olur, Allahu azimü'ş-şan'ın didârını 
    görür. 
    
    
